# Write a program to enter two integers and perform all arithmetic operations on them.
print('program to enter two integers and perform all arithmetic operations on them \n')
a = int(input('Enter a number'))
b = int(input('Enter another number'))
sum = a+b
sub = a-b
mul = a*b
div = a//b
mod = a%b
print('a =', a, 'b =', b, '\n a + b =', sum, '\n a - b =',
      sub, '\n a * b =', mul, '\n a / b =', div,'\n a % b =',mod)
